class User {
  int Id;
  String firstName;
  String secondName;
  String patronymic;
  String phone;
  String password;
  String photoUrl;
  String telegram;

  User({
    required this.Id,
    required this.firstName,
    required this.secondName,
    required this.patronymic,
    required this.phone,
    required this.password,
    required this.photoUrl,
    required this.telegram,
  });
}